package cursoProgramacaoA41_A57_EstruturasRepetitivas;

public class mainOperadoresBitWise {
    public static void main(String[] args) {
        int n1 = 89;   // 0101 1001
        int n2 = 60;   // 0011 1100

        System.out.println(n1 & n2);
        System.out.println(n1 | n2);
        System.out.println(n1 ^ n2);
    }
}
